public class Board {
    public String designator;

    public String getDesignator() {
        return designator;
    }

    public void setDesignator(String designator) {
        this.designator = designator;
    }

    public float x;

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float y;

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
}
